# 定义布尔变量is_adult = True、has_money = False；
# 计算：布尔值与数值的运算、比较运算结果、逻辑运算结果；
# 打印结果并验证布尔值的实际数值。

is_adult = True
has_money = False
# 布尔值的数值本质
print("True数值：", int(is_adult))
print("Flase数值", int(has_money))
print("True + 5", is_adult + 5)
# 比较运算（返回布尔值）
age = 18
print("age >= 18" , age >= 18)
print("age <= 18" , age <= 18)
# 逻辑运算
print("is_adult and has_money" , is_adult and has_money)
print("is_adult or has_money" , is_adult or has_money)
print("not is_adult" , not is_adult)

